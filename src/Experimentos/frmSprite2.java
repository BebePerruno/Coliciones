    private void jPanel1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPanel1KeyPressed
        x=jLUnidad.getX();
        y=jLUnidad.getY();
        switch(evt.getExtendedKeyCode()){
            case KeyEvent.VK_DOWN:
                if(jLUnidad.getY()>=10){
                    jLUnidad.setLocation(jLUnidad.getX(), jLUnidad.getY()+5);
                }else{
                    jLUnidad.setLocation(jLUnidad.getX(),10);
                }
                break;
            case KeyEvent.VK_UP:
                if(jLUnidad.getY()<=300){
                    jLUnidad.setLocation(jLUnidad.getX(), jLUnidad.getY()-5);
                }else{
                    jLUnidad.setLocation(jLUnidad.getX(), 300);
                }
                break;
            case KeyEvent.VK_RIGHT:
                if(jLUnidad.getX()<=750){
                    jLUnidad.setLocation(jLUnidad.getX()+5, jLUnidad.getY());
                }else{
                    jLUnidad.setLocation(800, jLUnidad.getY());
                }
                break;
                case KeyEvent.VK_LEFT:
                if(jLUnidad.getX()>=10){
                    jLUnidad.setLocation(jLUnidad.getX()-5, jLUnidad.getY());
                }else{
                    jLUnidad.setLocation(10, jLUnidad.getY());
                }
                break;
        }
    }//GEN-LAST:event_jPanel1KeyPressed

