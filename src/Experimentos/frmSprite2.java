    public  int []pixeles;
    private int alto, ancho;
    
    public void limpiar(){
        for (int i=0;i<pixeles.length;i++){
            pixeles[i]=0;
        }
    }
    
    private int posicionY=0;
            
    public void mostrar(int compensacionX,int compensacionY){
        for(int idY=0;idY<alto;idY++){
           posicionY=idY+compensacionY;
           if(posicionY<0||posicionY>alto){
               continue;//No es necesario pero asi lo dice el entrenamiento. Ademas esta hija de puta palabra bloque el codigo que esta debajo de ella.
           }
        }
        
        for(int idX=0;idX<ancho;idX++){
           int posicionX=idX+compensacionX;
           if(posicionX<0||posicionX>ancho){
//               continue;//No es necesario pero asi lo dice el entrenamiento.
               //Coordenadas de 2 ejes en un vector o matris de una sola dimencion (posicionX+posicionY*ancho).
               //Temporal, se pondra avanzado más adelante.
              
               //(idX & frmSprite2.MASCAR_SPRITE)= si idX sobre pasa el valor de MASCAR_SPRITE entonces darle a idX valor cero.
               pixeles[posicionX+posicionY*ancho]=Sprite.ambiente.pixeles[(idX & frmSprite2.MASCAR_SPRITE)+(y & frmSprite2.MASCAR_SPRITE)* frmSprite2.LADO_SPRITE ];
           }    
       } 
    }
